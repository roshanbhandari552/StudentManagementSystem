    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // Add student logic
      try {
        String firstName = firstnametxt.getText().trim();
        String lastName = lastnametxt.getText().trim();
        String email = emailtxt.getText().trim();
        String address = addresstxt.getText().trim();
        String ageText = agetxt.getText().trim();
        String genderText = gender.getSelectedItem().toString();
        String statusText = status.getSelectedItem().toString();

        // Create student object
        Student student = new Student();
        student.setFirstName(firstName);
        student.setLastName(lastName);
        student.setEmail(email);
        student.setAddress(address);
        student.setGender(genderText);
        student.setStatus(statusText);

        // Validate student data
      
       
         
        String validationMessage = controller.validateStudent(student);
        if (validationMessage != null) {
            showMessage(validationMessage);
             return;// Exit the method if validation fails
        }
         try {
            int age = Integer.parseInt(ageText);
            student.setAge(age);
        } catch (NumberFormatException e) {
            showMessage("Age must be a valid number.");
            return;
        }
        // Add student
        controller.addStudent(student);
        showMessage("Student added successfully.");
    } catch (Exception e) {
        showMessage("An unexpected error occurred: " + e.getMessage());
    }
    }//GEN-LAST:event_addbtnActionPerformed

