    private void viewdeactivebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewdeactivebtnActionPerformed
         // Get the table model
    DefaultTableModel tableModel = (DefaultTableModel) Table.getModel();
    
    // Loop through the table rows from the end to the beginning
    for (int i = tableModel.getRowCount() - 1; i >= 0; i--) {
        String status = tableModel.getValueAt(i, 7).toString(); // Column index 6 is the "Status" column
        if ("Active".equalsIgnoreCase(status)) {
            tableModel.removeRow(i); 
        }
    }
  
    }//GEN-LAST:event_viewdeactivebtnActionPerformed

